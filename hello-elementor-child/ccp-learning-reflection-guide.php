                Overview
            </h2>
            <p>The purpose of the learning summary and reflection is to help transform a completed learning activity into meaningful professional growth, which is essential to continuing competence. Evidence suggests that summarizing and reflecting can support retention because they require active engagement with the material. Through this process, learners retrieve information from memory, explain it in their own words, and connect it to real practice experiences. Active recall is strongly supported in educational research as a strategy for improving long-term retention.</p>
            <p>Continuing competence is not only about gaining knowledge; it is about applying learning to improve professional judgment, decision-making, communication, and patient outcomes. Reflection plays a role in this as it helps connect new knowledge to real practice situations, ethical responsibilities, professional standards, risks, and future actions.</p>
            <p>As a general guideline, a learning summary and reflection should include enough detail to demonstrate that meaningful professional growth has occurred. This means going beyond simply listing the learning activity completed or stating that new knowledge was gained. Including specific examples will strengthen your summary and reflection. The goal is to show that the activity led to more than passive participation; it contributed to reflective, practice-based development and a clear plan for applying the learning in a way that supports safe, effective, and competent care.</p>
